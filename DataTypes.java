package com.vstl;

public class DataTypes {

	public static void main(String[] args) 
	{
		//Data Types
		//1. boolean
		boolean blnFlag=true;
		//2. byte
		byte bytDefaultVal=0;
		//3. short
		short shrtVal=1000;
		//4. Int
		int intAge=25;
		//5. Long
		long longVal=1000000;
		//6. Float
		float floatDefaultVal=0.0f;
		//7. Double
		double dblVal=3.45;
		//8. Char
		char charletter='A';
		
		//For execution:
		System.out.println("boolean Value : "+blnFlag);
		System.out.println("byte defaultvalue : "+bytDefaultVal);
		System.out.println("short value : "+shrtVal);
		System.out.println("int value : "+intAge);
		System.out.println("long value : "+longVal);
		System.out.println("float defaultvalue : "+floatDefaultVal);
		System.out.println("double value : "+dblVal);
		System.out.println("char value : "+charletter);
	
		
		

	}

}
